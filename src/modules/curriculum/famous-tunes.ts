import type { FamousTune, SongTrack } from "@/modules/core";
import { parsePitch } from "@/modules/core";
import type { ExpectedNote } from "@/modules/core";

function n(
  label: string,
  startBeat: number,
  durationBeats: number,
  bpm: number,
  hand: "left" | "right" | "either" = "right",
  finger?: 1 | 2 | 3 | 4 | 5,
): ExpectedNote {
  const beatMs = 60000 / bpm;
  return {
    pitch: parsePitch(label),
    startMs: startBeat * beatMs,
    durationMs: durationBeats * beatMs,
    hand,
    finger,
  };
}

